// Addition
function add(a, b) {
  return a + b;
}

// Subtraction
function subtract(a, b) {
  return a - b;
}

// Multiplication
function multiply(a, b) {
  return a * b;
}

// Example usage
console.log("Add: ", add(5, 3));        // Output: 8
console.log("Subtract: ", subtract(5, 3)); // Output: 2
console.log("Multiply: ", multiply(5, 3)); // Output: 15

